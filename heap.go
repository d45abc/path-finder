package main

import "container/heap"

type Item struct {
	node  *Node
	dist  float32
	index int
}

type PriorityQueue []*Item

func (pq PriorityQueue) Len() int { return len(pq) }

func (pq PriorityQueue) Less(i, j int) bool {
	return pq[i].dist < pq[j].dist
}

func (pq PriorityQueue) Swap(i, j int) {
	pq[i], pq[j] = pq[j], pq[i]
	pq[i].index = i
	pq[j].index = j
}

func (pq *PriorityQueue) Push(x any) {
	n := len(*pq)
	item := x.(*Item)
	item.index = n
	*pq = append(*pq, item)
}

func (pq *PriorityQueue) Pop() any {
	old := *pq
	n := len(old)
	item := old[n-1]
	old[n-1] = nil
	item.index = -1
	*pq = old[0 : n-1]
	return item
}

func (pq *PriorityQueue) tryToUpdate(node *Node, dist float32) {
	for _, n := range *pq {
		if n.node == node {
			n.dist = dist
			heap.Fix(pq, n.index)
			return
		}
	}
	heap.Push(pq, &Item{
		node: node,
		dist: dist,
	})
}
